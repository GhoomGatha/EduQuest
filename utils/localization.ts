// FIX: Add and export the 'formatRelativeTime' function.
export const formatRelativeTime = (timestamp: string, lang: Language): string => {
    const date = new Date(timestamp);
    const now = new Date();
    const seconds = Math.round((now.getTime() - date.getTime()) / 1000);

    const rtf = new Intl.RelativeTimeFormat(lang, { numeric: 'auto' });

    if (Math.abs(seconds) < 60) {
        return rtf.format(-seconds, 'second');
    }
    const minutes = Math.round(seconds / 60);
    if (Math.abs(minutes) < 60) {
        return rtf.format(-minutes, 'minute');
    }
    const hours = Math.round(minutes / 60);
    if (Math.abs(hours) < 24) {
        return rtf.format(-hours, 'hour');
    }
    const days = Math.round(hours / 24);
    if (Math.abs(days) < 7) {
        return rtf.format(-days, 'day');
    }
    if (Math.abs(days) < 30) {
        const weeks = Math.round(days / 7);
        // Intl.RelativeTimeFormat doesn't support 'week' universally, so we fall back to days or use a polyfill if needed.
        // For simplicity, we'll just show days if it's less than a month.
        return rtf.format(-days, 'day');
    }
    const months = Math.round(days / 30);
    if (Math.abs(months) < 12) {
        return rtf.format(-months, 'month');
    }
    const years = Math.round(days / 365);
    return rtf.format(-years, 'year');
};